package com.spring.henallux.transpLux.model;


public class Language {
    private int id;

    private String name;

    public Language(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
